#include <iostream>
#include<string>
#include<math.h>

using namespace std;

int main()
{
    float km = 750;
    float h = 20;
    cout << "Amir pergi dari Jakarta ke Surabaya dengan menempuh jarak 750km. Dengan kendaraan roda 4 waktu tempuhnya adalah 20jam,\n";
    cout << "Hitunglah kecepatan kendaraan Amir tersebut !\n";
    cout << "Jarak tempuh = " << km << " km" << endl;
    cout << "Waktu tempuh = " << h << " h" << endl;
    cout << "Kecepatan rata-rata mobil Amir adalah = " << km / h << " km/h" << endl;
}

